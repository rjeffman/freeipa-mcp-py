"""Tests for vault cryptography operations."""

import base64
import os

import pytest

from freeipa_mcp.vault_crypto import (
    VaultCryptoError,
    derive_symmetric_key,
    fernet_decrypt,
    fernet_encrypt,
    generate_random_bytes,
    generate_vault_session_key,
    unwrap_vault_data,
    wrap_vault_data,
)


class TestPBKDF2:
    """Test PBKDF2 key derivation."""

    def test_derive_key_deterministic(self):
        """Same password and salt produce same key."""
        key1 = derive_symmetric_key(b"password123", b"saltsalt")
        key2 = derive_symmetric_key(b"password123", b"saltsalt")
        assert key1 == key2

    def test_derive_key_length(self):
        """Derived key is 32 bytes."""
        key = derive_symmetric_key(b"password", b"salt")
        assert len(key) == 32

    def test_derive_key_salt_sensitive(self):
        """Different salts produce different keys."""
        key1 = derive_symmetric_key(b"password", b"salt1")
        key2 = derive_symmetric_key(b"password", b"salt2")
        assert key1 != key2

    def test_derive_key_password_sensitive(self):
        """Different passwords produce different keys."""
        key1 = derive_symmetric_key(b"password1", b"salt")
        key2 = derive_symmetric_key(b"password2", b"salt")
        assert key1 != key2


class TestFernet:
    """Test Fernet encryption/decryption."""

    def test_fernet_roundtrip(self):
        """Encrypt and decrypt recovers original data."""
        key = derive_symmetric_key(b"testpass", b"testsalt")
        plaintext = b"secret vault data"
        token = fernet_encrypt(plaintext, key)
        recovered = fernet_decrypt(token, key)
        assert recovered == plaintext

    def test_fernet_token_is_base64(self):
        """Fernet token is valid base64."""
        key = derive_symmetric_key(b"pass", b"salt")
        token = fernet_encrypt(b"data", key)
        # Should be decodable
        decoded = base64.urlsafe_b64decode(token)
        assert len(decoded) > 0

    def test_fernet_wrong_key_fails(self):
        """Decryption with wrong key fails."""
        key1 = derive_symmetric_key(b"key1", b"salt")
        key2 = derive_symmetric_key(b"key2", b"salt")
        token = fernet_encrypt(b"secret", key1)
        with pytest.raises(VaultCryptoError):
            fernet_decrypt(token, key2)

    def test_fernet_invalid_key_length(self):
        """Fernet rejects keys that aren't 32 bytes."""
        with pytest.raises(VaultCryptoError, match="32 bytes"):
            fernet_encrypt(b"data", b"shortkey")

    def test_fernet_invalid_token(self):
        """Fernet rejects invalid tokens."""
        key = derive_symmetric_key(b"pass", b"salt")
        with pytest.raises(VaultCryptoError):
            fernet_decrypt(b"invalid_token_data", key)

    def test_fernet_empty_data(self):
        """Fernet handles empty data."""
        key = derive_symmetric_key(b"pass", b"salt")
        token = fernet_encrypt(b"", key)
        recovered = fernet_decrypt(token, key)
        assert recovered == b""

    def test_fernet_large_data(self):
        """Fernet handles larger data."""
        key = derive_symmetric_key(b"pass", b"salt")
        plaintext = b"x" * 10000
        token = fernet_encrypt(plaintext, key)
        recovered = fernet_decrypt(token, key)
        assert recovered == plaintext


