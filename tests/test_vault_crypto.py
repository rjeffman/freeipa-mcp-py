class TestDataWrapping:
    """Test CBC data wrapping/unwrapping."""

    def test_wrap_unwrap_aes128(self):
        """AES-128-CBC wrap/unwrap roundtrip."""
        session_key = generate_vault_session_key("aes-128-cbc")
        data = b"vault data to encrypt"
        nonce, ciphertext = wrap_vault_data("aes-128-cbc", session_key, data)

        assert len(nonce) == 16  # AES IV is 16 bytes
        assert len(ciphertext) > len(data)  # Includes padding

        recovered = unwrap_vault_data("aes-128-cbc", session_key, nonce, ciphertext)
        assert recovered == data

    def test_wrap_unwrap_3des(self):
        """3DES wrap/unwrap roundtrip."""
        session_key = generate_vault_session_key("des-ede3-cbc")
        data = b"more vault data"
        nonce, ciphertext = wrap_vault_data("des-ede3-cbc", session_key, data)

        assert len(nonce) == 8  # 3DES IV is 8 bytes

        recovered = unwrap_vault_data("des-ede3-cbc", session_key, nonce, ciphertext)
        assert recovered == data

    def test_wrap_empty_data(self):
        """Wrapping empty data works."""
        session_key = generate_vault_session_key("aes-128-cbc")
        nonce, ciphertext = wrap_vault_data("aes-128-cbc", session_key, b"")

        # Even empty data gets padded
        assert len(ciphertext) > 0

        recovered = unwrap_vault_data("aes-128-cbc", session_key, nonce, ciphertext)
        assert recovered == b""

    def test_wrap_large_data(self):
        """Wrapping large data works."""
        session_key = generate_vault_session_key("aes-128-cbc")
        data = os.urandom(50000)
        nonce, ciphertext = wrap_vault_data("aes-128-cbc", session_key, data)
        recovered = unwrap_vault_data("aes-128-cbc", session_key, nonce, ciphertext)
        assert recovered == data

    def test_unwrap_wrong_key(self):
        """Unwrapping with wrong key fails."""
        key1 = generate_vault_session_key("aes-128-cbc")
        key2 = generate_vault_session_key("aes-128-cbc")
        nonce, ciphertext = wrap_vault_data("aes-128-cbc", key1, b"data")

        with pytest.raises(VaultCryptoError):
            unwrap_vault_data("aes-128-cbc", key2, nonce, ciphertext)

    def test_unwrap_wrong_nonce(self):
        """Unwrapping with wrong nonce produces garbage."""
        session_key = generate_vault_session_key("aes-128-cbc")
        nonce1, ciphertext = wrap_vault_data("aes-128-cbc", session_key, b"data")
        nonce2 = generate_random_bytes(16)

        # Wrong nonce produces invalid padding
        with pytest.raises(VaultCryptoError, match="padding"):
            unwrap_vault_data("aes-128-cbc", session_key, nonce2, ciphertext)

    def test_wrap_unknown_algorithm(self):
        """Unknown algorithm raises error."""
        session_key = generate_random_bytes(32)
        with pytest.raises(VaultCryptoError, match="Unknown vault wrapping"):
            wrap_vault_data("aes-256-gcm", session_key, b"data")

    def test_unwrap_unknown_algorithm(self):
        """Unknown algorithm raises error."""
        session_key = generate_random_bytes(32)
        nonce = generate_random_bytes(16)
        with pytest.raises(VaultCryptoError, match="Unknown vault wrapping"):
            unwrap_vault_data("aes-256-gcm", session_key, nonce, b"data")

    def test_wrap_nonce_is_random(self):
        """Each wrap generates unique nonce."""
        session_key = generate_vault_session_key("aes-128-cbc")
        nonce1, _ = wrap_vault_data("aes-128-cbc", session_key, b"data")
        nonce2, _ = wrap_vault_data("aes-128-cbc", session_key, b"data")
        assert nonce1 != nonce2
